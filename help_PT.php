<h3> 
Abaixo encontra-se uma s&iacute;ntese dos principais par&acirc;metros
utilizados na busca de imagens no Cat&aacute;logo. Desejando informa&ccedil;&atilde;o 
mais detalhada consulte o <a target = "_blank" HREF = "../Suporte/files/manual_usuario_PT.htm" style="font-size:15">Manual do Usu&aacute;rio.</a>
</h3>
<br> 
<p>
No painel ao lado voc&ecirc; pode definir par&acirc;metros
b&aacute;sicos e espec&iacute;ficos para procurar imagens no Cat&aacute;logo.

<br>
Como par&acirc;metros b&aacute;sicos h&aacute;:
<br>
<li style="font-size:15">
<b>Sat&eacute;lite</b> - escolha um determinado sat&eacute;lite ou deixe em branco para
procurar imagens de qualquer sat&eacute;lite.
<li style="font-size:15">
<b>Instrumento</b> - caso voc&ecirc; tenha escolhido um sat&eacute;lite, poder&aacute; escolher
um determinado instrumento deste sat&eacute;lite ou deixar em branco para procurar
imagens de todos os instrumentos deste sat&eacute;lite.
<li style="font-size:15">
<b>Intervalo de Tempo</b> - voc&ecirc; pode escolher um intervalo de tempo corrido, por exemplo
de 10/03/1998 at&eacute; 20/05/2003, ou ent&atilde;o um intervalo <u>Sazonal</u> (que N&Atilde;O LEVA EM CONTA os valores dos dias, APENAS OS MESES do intervalo), que para o mesmo per&iacute;odo
(de 10/03/1998 at&eacute; 20/05/2003) significa que somente imagens obtidas de mar&ccedil;o at&eacute; maio dos
anos de 1998 ate 2003 ser&atilde;o selecionadas.
<li style="font-size:15">
<b>Cobertura M&aacute;xima de Nuvens </b> - por quadrante da imagem, voc&ecirc; pode definir o m&aacute;ximo
de cobertura de nuvens desejado para a sua pesquisa. Se deixados em branco, qualquer
valor de cobertura &eacute; aceit&aacute;vel.<br>
Os quadrantes s&atilde;o definidos assim :
