<tr>
<td>Q1</td><td>Q2</td>
</tr><tr>
<td>Q3</td><td>Q4</td>
</tr>
</table>
</center></div> 
<br>
Estes s&atilde;o os par&acirc;metros b&aacute;sicos. Voc&ecirc; tem quatro formas de consulta que podem
ser usadas de acordo com sua necessidade. Lembre-se de que para todas as formas de consulta
os par&acirc;metros b&aacute;sicos ser&atilde;o levados em conta.
<br><br>
<center><h3>Modalidades de busca.</h3></center>
<br>
<li style="font-size:15">
<b>Por Munic&iacute;pio</b> - se voc&ecirc; sabe o nome do munic&iacute;pio de seu interesse, entre com o nome completo ou parte
dele no campo Munic&iacute;pio e o Estado se desejar restringir sua pesquisa e clique em Executar. Os munic&iacute;pios que atendem sua consulta
aparecer&atilde;o neste quadro. Clique em um nome de munic&iacute;pio e voc&ecirc; ser&aacute; levado a interface
gr&aacute;fica do item anterior. A sede do munic&iacute;pio sera marcada com um c&iacute;rculo vermelho. As imagens dispon&iacute;veis s&atilde;o indicadas pelo &iacute;cono respectivo, que representa o centro de cada cena.
<br><br>
<li style="font-size:15">
<b>Por &Oacute;rbita/Ponto</b> - todo sat&eacute;lite tem um sistema de refer&ecirc;ncia baseado em &oacute;rbitas e pontos,
que variam de fam&iacute;lia para fam&iacute;lia de sat&eacute;lites. Por exemplo a cidade de Bras&iacute;lia est&aacute; contida
na &oacute;rbita 157, ponto 118 para a fam&iacute;lia de sat&eacute;lites CBERS. Se voc&ecirc; sabe exatamente
a &oacute;rbita e o ponto de seu interesse, entre com os valores nos campos <b>De</b>
e clique em Executar. Se voc&ecirc; quiser pesquisar uma faixa de valores para &oacute;rbitas e pontos,
entre com o valor menor no campo <b>De</b> e o maior no campo <b>At&eacute;</b> e clique em Executar
<br><br>
<li style="font-size:15">
<b>Por Regi&atilde;o </b>- se voc&ecirc; tem uma regi&atilde;o de interesse, definida em coordenadas geogr&aacute;ficas,
ou seja em latitudes e longitudes, entre com a latitude Norte no campo <b>Norte</b>, a
latitude Sul no campo <b>Sul</b>, a longitude Oeste no campo <b>Oeste</b> e a longitude Leste
no campo <b>Leste</b> e clique em Executar.
<br><br>
<li style="font-size:15">
<b>Interface Gr&aacute;fica </b>- se voc&ecirc; desejar navegar geograficamente, entre com as coordenadas
do seu ponto central em latitude e longitude nos campos <b>Lat</b> e <b>Lon</b> e clique em Executar. Voc&ecirc; ter&aacute; uma
interface gr&aacute;fica com imagens no fundo e o mapa do Brasil com a divis&atilde;o por estados e com o nome de
algumas cidades para sua orienta&ccedil;&atilde;o. Clicando nos bot&otilde;es de dire&ccedil;&atilde;o nas margens
da interface voc&ecirc; pode deslocar a &aacute;rea mostrada.
