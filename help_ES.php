Estos son los par&aacute;metros b&aacute;sicos. Usted tiene cuatro formas de consulta que pueden
ser utilizadas de acuerdo con su necesidad. Recuerde que para todas las formas
de consulta ser&aacute;n considerados los par&aacute;metros b&aacute;sicos. <o:p></o:p></span></p>

<h3 align=center style='text-align:center'>Modalidades <span lang=ES
style='mso-ansi-language:ES'>de b&uacute;squeda.<o:p></o:p></span></h3>

<p class=MsoNormal><![if !supportEmptyParas]>&nbsp;<![endif]><o:p></o:p></p>

<li style="font-size:15">
<b>Por
Municipio</b> - si usted sabe el nombre del municipio de su inter&eacute;s, entre con
el nombre completo o parte del mismo en el campo Municipio y si desea
restringir su b&uacute;squeda coloque tambi&eacute;n el Estado al cual pertenece este
municipio y haga clic en Ejecutar. Los municipios que satisfacen su consulta
aparecer&aacute;n en este cuadro. Haga clic en uno de los nombres de municipio que
fueron listados y se presentar&aacute; la interfaz gr&aacute;fica. La sede del municipio ser&aacute;
marcada con un c&iacute;rculo rojo. Las im&aacute;genes disponibles son
indicadas por iconos de diferentes colores que representan el centro de cada escena.

<li style="font-size:15">
<b>Por Path/Row</b> - todo sat&eacute;lite tiene un sistema de referencia basado en &oacute;rbitas y
puntos, que varian de familia para familia de sat&eacute;lites. Por ejemplo, la ciudad
de Brasília est&aacute; en el path (&oacute;rbita) 157, row (punto) 118 para la familia de
sat&eacute;lites CBERS. Se usted sabe exactamente el path y el row de su inter&eacute;s,
entre con los valores en los campos <b>De</b> y haga clic en Ejecutar. Se usted
desea buscar un rango de paths y rows, entre con el menor valor en el campo <b>De</b>
y con el mayor en el campos <b>Hasta</b> y haga clic en Ejecutar. 

<li style="font-size:15">
<b>Por Regi&oacute;n </b> - si usted tiene inter&eacute;s por una determinada regi&oacute;n, definida en
coordenadas geogr&aacute;ficas, o sea en latitudes y longitudes, entre con la latitud
Norte en el campo <b>Norte</b>, la latitud Sur en el campo <b>Sur</b>, la
longitud Oeste en el campo <b>Oeste</b> y con la longitud Este no campo E<b>ste</b>
y haga clic en Ejecutar.

<li style="font-size:15">
<b>Interfaz Gr&aacute;fica </b> - se usted desea navegar geogr&aacute;ficamente, entre con las coordenadas
del punto central en latitud y longitud en los campos <b>Lat</b> y <b>Lon</b> y
haga clic en Ejecutar. Usted tendr&aacute; una interfaz
gr&aacute;fica con im&aacute;genes en el fondo con la divisi&oacute;n pol&iacute;tico administrativa y con
el nombre de algunas ciudades para su orientaci&oacute;n. Al hacer clic en los
botones de direcci&oacute;n localizados en las m&aacute;rgenes de la interfaz usted podr&aacute;
desplazar el &aacute;rea visualizada. </p>

