<h3><span lang=ES style='mso-ansi-language:ES'>A seguir se presenta una s&iacute;ntesis
de los principales par&aacute;metros utilizados en la b&uacute;squeda de im&aacute;genes en el Cat&aacute;logo.
Si desea informaci&oacute;n m&aacute;s detallada consulte el <a
href="../Suporte/files/manual_usuario_ES.htm" target="_blank" style="font-size:15">Manual
del Usuario.</a> <o:p></o:p></span></h3>

<p class=MsoNormal><span lang=ES style='mso-ansi-language:ES'><![if !supportEmptyParas]>&nbsp;<![endif]><o:p></o:p></span></p>

<p><span lang=ES style='mso-ansi-language:ES'>En el men&uacute; al lado usted puede
definir par&aacute;metros b&aacute;sicos y espec&iacute;ficos para buscar im&aacute;genes en el Cat&aacute;logo. <br>
Los par&aacute;metros b&aacute;sicos disponibles son: <o:p></o:p></span></p>

<li style="font-size:15">
<b>Sat&eacute;lite</b> - selecciones un determinado sat&eacute;lite o deje el campo en blanco para buscar im&aacute;genes de cualquier
sat&eacute;lite.

<li style="font-size:15">
<b>Instrumento</b> - en el caso que haya seleccionado un sat&eacute;lite, usted podr&aacute; escoger un determinado
instrumento de ese sat&eacute;lite o puede dejar en blanco para buscar im&aacute;genes de
todos los instrumentos del sat&eacute;lite seleccionado.

<li style="font-size:15">
<b>Intervalo de Tiempo</b> - usted puede escoger un intervalo de tiempo corrido, por ejemplo de 10/03/1998 hasta 20/05/2003,
o sino un intervalo <u>Estacional</u> (que NO considera los dias, SOLAMENTE los meses) que para el mismo per&iacute;odo (de 10/03/1998 hasta
20/05/2003) significa que solamente ser&aacute;n seleccionadas im&aacute;genes obtenidas de marzo
a mayo de los a&ntilde;os de 1998 hasta 2003. 

<li style="font-size:15">
<b>Cobertura M&aacute;xima de Nubes </b> - por cuadrante de la imagen, usted puede definir el m&aacute;ximo de cobertura de nubes
deseado. Si se dejan en blanco, significa que es aceptable cualquier valor de
cobertura de nubes.<br>
Los cuadrantes son definidos de la siguiente manera:

