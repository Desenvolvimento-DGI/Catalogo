<p></p>
<h3> Vous trouverez ci-dessous une synthèse des principaux paramètres
utilisés pour la recherche d'images dans le Catalogue. Si vous désirez
des informations plus détaillées consultez le <a target="_blank" href="../Suporte/files/manual_usuario_FR.htm" style="font-size: 15px;">Manuel de l'Utilisateur.</a>
</h3>
<br>
<p>Grâce au panneau sur le coté, vous pouvez définir les
paramètres
basiques et spécifiques pour chercher des images dans le Catalogue.
<br>
Comme paramètres basiques, il y a:
<br>
</p>
<li style="font-size: 15px;"><b>Satellite</b>
-
Choisissez un Satellite déterminé ou laissez en blance pour
rechercher les images d'un quelconque Satellite.
</li>
<li style="font-size: 15px;"><b>Instrument</b>
- Si vous avez choisi un Satellite, vous pourrez choisir un instrument
déterminé de ce Satellite ou laisser en blanc pour
rechercher
les images de tous les instruments de ce Satellite.
</li>
<li style="font-size: 15px;"><b>Intervalle de Temps</b>
- vous pouvez choisir un intervalle de temps, par exemple
de 10/03/1998 à 20/05/2003, ou alors un intervalle <u>Saisonnier</u>
(qui NE TIENT PAS COMPTE des valeurs des jours, JUSTE DES MOIS de
l'intervalle), ce qui signifie que pour la même période
(de 10/03/1998 à 20/05/2003) seules les images obtenues de
mars à mai des
années de 1998 à 2003 seront sélectionnées.
</li>
<li style="font-size: 15px;"><b>Couverture Nuageuse
Maximale </b> - par quadrante de l'image, vous pouvez définir la
couverture nuageuse maximale désirée pour votre recherche. Si elle est
laissée en
blanc, toute
valeur de Couverture est acceptable.<br>
Les quadrantes sont définis ainsi :
<div>
<center>
<table>
<tbody>
<tr>
<td>Q1</td>
<td>Q2</td>
</tr>
<tr>
<td>Q3</td>
<td>Q4</td>
</tr>
</tbody>
</table>
</center>
</div>
<br>
Il s'agit des paramètres basiques. Vous avez quatre modes de recherche
qui
peuvent être utilisés en accord avec vos besoins. Rappelez-vous que
pour tous les modes de recherche, les paramètres basiques seront pris
en compte.
<br>
<br>
<center>
<h3>Modalités de recherche.</h3>
</center>
<br>
</li>
<li style="font-size: 15px;"><b>Par Municipalité</b>
- si vous connaissez le nom de la municipalité qui vous intéresse,
entrez le nom
complet ou une partie
dans le champ municipalité et l'Etat si vous désirez restreindre votre
recherche et
cliquez sur Exécuter. Les municipalités qui répondent à votre requête
apparaîtront dans ce tableau. Cliquez sur un nom de municipalité et
vous serez
amené à l'interface
graphique de l'article antérieur. Le siège de la municipalité sera
marqué avec un cercle rouge. Les images disponibles sont indiquées par
l'icône
respectif, qui représente le centre de chaque scène.
<br>
<br>
</li>
<li style="font-size: 15px;"><b>Par Orbite/Point</b>
- tout Satellite possède un système de référence basé sur des Orbites
et des
Points,
qui varient de famille en famille de Satellites. Par exemple la ville
de Brasília est contenue
dans l'Orbite 157, Point 118 pour la famille de Satellites CBERS. Si
vous connaissez
exactement
l'Orbite et le Point qui vous intéressent, entrez les valeurs dans le
champs <b>De</b>
et cliquez sur Exécuter. Si vous voulez chercher une bande de valeurs
pour des Orbites et des Points,
entrez la valeur la plus petite dans le champ <b>De</b> et
la plus grande dans le
champ <b>A</b> et cliquez sur Exécuter
<br>
<br>
</li>
<li style="font-size: 15px;"><b>Par Région </b>-
si vous avez une région qui vous intéresse, définie en coordonnées
géographiques,
c'est-à-dire en latitudes et longitudes, entrez la latitude Nord dans
le champ <b>Nord</b>,
la
latitude Sud dans le champ <b>Sud</b>, la longitude Ouest
dans le
champ <b>Ouest</b> et la longitude Est
dans le champ <b>Est</b> et cliquez sur Exécuter.
<br>
<br>
</li>
<li style="font-size: 15px;"><b>Interface graphique </b>-
si vous désirez naviguer géographiquement, entrez les coordonnées
de votre Point central en latitude et longitude dans les champs <b>Lat</b>
et <b>Lon</b> et cliquez sur Exécuter. Vous aurez une
interface graphique avec des images dans le fond et la carte du Brésil
avec la
division
par états et avec le nom de quelques villes pour vous orienter. En
cliquant sur les touches de direction
dans les
marges
de l'interface vous pouvez déplacer la zone affichée.
